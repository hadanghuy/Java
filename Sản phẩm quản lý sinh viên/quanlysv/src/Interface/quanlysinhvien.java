    public void FillTable(JTable table, String MysqltableName) throws ClassNotFoundException, SQLException
    {
        connect conn = new connect();
        ResultSet rs = conn.getData(MysqltableName);
        while(table.getRowCount() > 0) 
        {
            //JOptionPane.showMessageDialog(null, table.getRowCount());
            ((DefaultTableModel) table.getModel()).setRowCount(0);
            //((DefaultTableModel) table.getModel()).removeRow(0);
        }
        int columns = rs.getMetaData().getColumnCount();
        while(rs.next())
        {  
            Object[] row = new Object[columns];
            for (int i = 1; i <= columns; i++)
            {  
                if(i == 4)
                {
                    if (rs.getBoolean(i)) {row[i - 1] = "Nam";}
                    else {row[i - 1] = "Nữ";}
                    continue;
                }
                if(i==5)
                {
                    SimpleDateFormat date = new SimpleDateFormat(OLD_FORMAT);
                    try {
                        java.util.Date datestr = null;
                        datestr = date.parse(rs.getString(i));
                        date.applyPattern(NEW_FORMAT);
                        row[i - 1] = date.format(datestr);
                    continue;
                    } catch (ParseException ex) {
                        java.util.logging.Logger.getLogger(quanlysinhvien.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                    }
                }
                row[i - 1] = rs.getObject(i);
            }
            ((DefaultTableModel) table.getModel()).insertRow(rs.getRow()-1,row);
        }
    }
