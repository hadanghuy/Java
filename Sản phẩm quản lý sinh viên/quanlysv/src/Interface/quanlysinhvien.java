    private void TabledataMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabledataMousePressed
        //TODO add your handling code here:
       txtFieldID.setText(Integer.toString((int)Tabledata.getValueAt(Tabledata.getSelectedRow() , 0)));
        txtFieldName.setText((String)Tabledata.getValueAt(Tabledata.getSelectedRow() , 1));
       txtmalop.setText((String)Tabledata.getValueAt(Tabledata.getSelectedRow() , 2));
        if(Tabledata.getValueAt(Tabledata.getSelectedRow() , 3).equals("Nam")){
            radiobtnMale.setSelected(true);
        }else{
            radiobtnFemale.setSelected(true);
        }
        String datestring = (String)Tabledata.getValueAt(Tabledata.getSelectedRow(), 4);
        SimpleDateFormat datefomat = new SimpleDateFormat(NEW_FORMAT);
        java.util.Date date;
        try {
            date = datefomat.parse(datestring);
            txtFieldDay.setText(Integer.toString(date.getDate()));
            txtFieldMonth.setText(Integer.toString(date.getMonth()+1));
            txtFieldYear.setText(Integer.toString(date.getYear()+1900));
        } catch (ParseException ex) {
            java.util.logging.Logger.getLogger(quanlysinhvien.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        txtFieldethnic.setText((String)Tabledata.getValueAt(Tabledata.getSelectedRow() , 5));
        txtFieldaddress.setText((String)Tabledata.getValueAt(Tabledata.getSelectedRow() , 6));
    }//GEN-LAST:event_TabledataMousePressed

